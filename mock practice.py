i = int(input("Enter a number:" ))
def Number_Odd_or_Even():         #function to check if the number is odd or even
     
    """Checks whether the number is odd or even."""
    if i%2==0:
        print("The number is even")
    else:
        print("The number is odd")
Number_Odd_or_Even()

